graph = {
    'S': {'A': 3, 'B': 1},
    'A': {'S': 3, 'B': 2, 'C': 2},
    'B': {'S': 1, 'A': 2, 'C': 3},
    'C': {'A': 2, 'B': 3, 'D': 3, 'G': 4},
    'D': {'C': 4, 'G': 1},
    'G': {'C': 4, 'D': 1}
}

heuristic_costs = {
    'S': 7,
    'A': 5,
    'B': 7,
    'C': 4,
    'D': 1,
    'G': 0
}

# Print the graph
print("Graph:")
for node, neighbors in graph.items():
    print(f"{node} : {neighbors}")

# Print the heuristic costs
print("\nHeuristic Costs:")
for node, cost in heuristic_costs.items():
    print(f"{node} : {cost}")
